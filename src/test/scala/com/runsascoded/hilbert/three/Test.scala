package com.runsascoded.hilbert.three

import hilbert._

class Test
  extends hammerlab.Suite {
  test("[ 0,  8)") {
    ==(`3`(0), (0, 0, 0))
    ==(`3`(1), (1, 0, 0))
    ==(`3`(2), (1, 1, 0))
    ==(`3`(3), (0, 1, 0))
    ==(`3`(4), (0, 1, 1))
    ==(`3`(5), (1, 1, 1))
    ==(`3`(6), (1, 0, 1))
    ==(`3`(7), (0, 0, 1))
  }

  test("[ 8, 16)") {
    ==(`3`( 8), (0, 0, 2))
    ==(`3`( 9), (0, 1, 2))
    ==(`3`(10), (0, 1, 3))
    ==(`3`(11), (0, 0, 3))
    ==(`3`(12), (1, 0, 3))
    ==(`3`(13), (1, 1, 3))
    ==(`3`(14), (1, 1, 2))
    ==(`3`(15), (1, 0, 2))
  }

  test("[16, 24)") {
    ==(`3`(16), (2, 0, 2))
    ==(`3`(17), (2, 1, 2))
    ==(`3`(18), (2, 1, 3))
    ==(`3`(19), (2, 0, 3))
    ==(`3`(20), (3, 0, 3))
    ==(`3`(21), (3, 1, 3))
    ==(`3`(22), (3, 1, 2))
    ==(`3`(23), (3, 0, 2))
  }

  test("[24, 32)") {
    ==(`3`(24), (3, 0, 1))
    ==(`3`(25), (2, 0, 1))
    ==(`3`(26), (2, 0, 0))
    ==(`3`(27), (3, 0, 0))
    ==(`3`(28), (3, 1, 0))
    ==(`3`(29), (2, 1, 0))
    ==(`3`(30), (2, 1, 1))
    ==(`3`(31), (3, 1, 1))
  }

  test("[32, 40)") {
    ==(`3`(32), (3, 2, 1))
    ==(`3`(33), (2, 2, 1))
    ==(`3`(34), (2, 2, 0))
    ==(`3`(35), (3, 2, 0))
    ==(`3`(36), (3, 3, 0))
    ==(`3`(37), (2, 3, 0))
    ==(`3`(38), (2, 3, 1))
    ==(`3`(39), (3, 3, 1))
  }

  test("[40, 48)") {
    ==(`3`(40), (3, 3, 2))
    ==(`3`(41), (3, 2, 2))
    ==(`3`(42), (3, 2, 3))
    ==(`3`(43), (3, 3, 3))
    ==(`3`(44), (2, 3, 3))
    ==(`3`(45), (2, 2, 3))
    ==(`3`(46), (2, 2, 2))
    ==(`3`(47), (2, 3, 2))
  }

  test("[48, 56)") {
    ==(`3`(48), (1, 3, 2))
    ==(`3`(49), (1, 2, 2))
    ==(`3`(50), (1, 2, 3))
    ==(`3`(51), (1, 3, 3))
    ==(`3`(52), (0, 3, 3))
    ==(`3`(53), (0, 2, 3))
    ==(`3`(54), (0, 2, 2))
    ==(`3`(55), (0, 3, 2))
  }

  test("[56, 64)") {
    ==(`3`(56), (0, 3, 1))
    ==(`3`(57), (1, 3, 1))
    ==(`3`(58), (1, 2, 1))
    ==(`3`(59), (0, 2, 1))
    ==(`3`(60), (0, 2, 0))
    ==(`3`(61), (1, 2, 0))
    ==(`3`(62), (1, 3, 0))
    ==(`3`(63), (0, 3, 0))
  }
}
